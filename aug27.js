// FOR loop: Print numbers from 1 to 5
console.log("FOR loop:");
for (let i = 1; i <= 5; i++) {
  console.log(i);
}

// WHILE loop: Print numbers from 1 to 5
console.log("\nWHILE loop:");
let j = 1;
while (j <= 5) {
  console.log(j);
  j++;
}

// DO...WHILE loop: Print numbers from 1 to 5
console.log("\nDO...WHILE loop:");
let k = 1;
do {
  console.log(k);
  k++;
} while (k <= 5);
