

function sumTwoNumbers(a, b,) {
    let sum = 0;
   

    // Add first number
    for (let i = 0; i < a; i++) {
        sum += 1;
    }

    // Add second number
    for (let j = 0; j < b; j++) {
        sum += 1;
    }
    
   

    console.log(sum);
}

// Example usage
sumTwoNumbers(3, 4);  // Output: 7




